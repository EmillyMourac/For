// Crie um programa que mostre os números pares de 2 até 20 usando for. 
// (proíbido o uso de foreach ou qualquer outro tipo, é permitido 
// somente o uso de for)

public static class Atv3for
{
    public static void Executar()
    {
        for (int contador = 2; contador <= 20; contador += 2)
        {
            Console.WriteLine(contador);
        }
    }
}